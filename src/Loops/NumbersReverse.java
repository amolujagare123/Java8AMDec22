package Loops;

public class NumbersReverse {

    public static void main(String[] args) {
        // print 10 to 1 numbers

        /* 1. Initialization
           2. Condition checking
          3. Increment / decrement
*/

        int  i = 10;

        while (i>=1)
        {
            System.out.println(i);
            i = i - 1; // i-- / --i
        }
    }
}
