package Loops;

public class Numbers {

    public static void main(String[] args) {
        // print 1 to 10 numbers

        /* 1. Initialization
           2. Condition checking
          3. Increment / decrement
*/

        int  i = 1;

        while (i<=10)
        {
            System.out.println(i);
            i = i + 1;
        }
    }
}
