package Arrays.SingleDimentional;

public class ArrayStringDemo {

    public static void main(String[] args) {

        String[] a = new String[6];

        a[0] = "Abhijeet";
        a[1] = "Aditi";
        a[2] = "Aishwarya";
        a[3] = "Ankita";
        a[4] = "Bharat";
        a[5] = "Darshan";


        for(int i=0;i<a.length;i++)
            System.out.println(a[i]);

    }
}
