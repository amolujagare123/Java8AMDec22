package Arrays.SingleDimentional;

public class ArrayCharDemo {

    public static void main(String[] args) {

        char[] a = new char[6];

        a[0] = 'a';
        a[1] = 'b';
        a[2] = 'c';
        a[3] = 'd';
        a[4] = 'e';
        a[5] = 'f';


        for(int i=0;i<a.length;i++)
            System.out.println(a[i]);

    }
}
