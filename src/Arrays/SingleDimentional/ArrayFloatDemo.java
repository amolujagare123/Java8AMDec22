package Arrays.SingleDimentional;

public class ArrayFloatDemo {

    public static void main(String[] args) {

        float[] a = new float[6];

        a[0] = 45.89f;
        a[1] = 67.63f;
        a[2] = 87.11f;
        a[3] = 89.90f;
        a[4] = 12.11f;
        a[5] = 9.8f;


        for(int i=0;i<a.length;i++)
            System.out.println(a[i]);

    }
}
