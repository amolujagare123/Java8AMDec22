package Arrays.SingleDimentional;

public class ArrayDoubleDemo {

    public static void main(String[] args) {

        double[] a = new double[6];

        a[0] = 45.89;
        a[1] = 67.63;
        a[2] = 87.11;
        a[3] = 89.90;
        a[4] = 12.11;
        a[5] = 9.8;


        for(int i=0;i<a.length;i++)
            System.out.println(a[i]);

    }
}
